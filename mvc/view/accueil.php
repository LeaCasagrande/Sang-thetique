<!-- Notre mission -->
<div class="part fonce" id="mission">
    <h3>Notre mission</h3>
    <p>Sang-thétique se concentre sur la production de sang synthétique. L'objectif principal est de répondre aux besoins en sang en remplaçant les dons de sang. Pour atteindre ce but, nos équipes travaillent sur la mise au point d'une solution viable pour produire du sang en quantité suffisante pour répondre aux demandes en cas de besoin. Cela nécessite une compréhension approfondie des processus biologiques impliqués dans la production de sang et de nombreux tests pour s'assurer de la qualité et de la sécurité du sang synthétique produit. Notre laboratoire utilise les dernières techniques et technologies pour conduire ses recherches dans ce domaine passionnant de la biologie humaine.</p><br>

    <p>La production de sang synthétique peut être accomplie de différentes façons :</p><br>
    <ul style="list-style-type: '\1FA78';">
        <li>Culture de cellules souches : Les cellules souches peuvent être cultivées en laboratoire et différenciées en différents types de cellules sanguines, telles que les globules rouges et les plaquettes.</li><br>
        <li>Thérapie génique : Les anomalies génétiques associées à des troubles sanguins peuvent être corrigées par thérapie génique en utilisant des vecteurs viraux pour introduire de nouveaux gènes fonctionnels dans les cellules souches ou les cellules sanguines.</li><br>
        <li>Bioprinting : La bioprinting peut être utilisée pour produire des structures sanguines en 3D en utilisant des bio-imprimantes pour déposer des cellules et des matériaux biologiques sur un substrat.</li><br>
        <li>Ingénierie tissulaire : Les techniques d'ingénierie tissulaire peuvent être utilisées pour améliorer la qualité et la fonction des produits sanguins synthétiques en utilisant des matériaux biocompatibles pour soutenir la croissance et la maturation des cellules sanguines produites.</li>
    </ul><br>
</div>

