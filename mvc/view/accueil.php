<div class="part">
    <h2>Notre expertise</h2>
    <p class="paragraphe">Chez Sang-thétique, nos scientifiques sont spécialisés dans la production de cellules sanguines synthétiques. Notre expertise se situe dans la compréhension approfondie de la biologie et de la génétique des globules rouges, blancs, du plasma et des plaquettes. Nous travaillons avec des techniques de pointe pour produire des cellules sanguines artificielles, tout en étudiant leur fonctionnement et leur comportement. </p>
    <p class="paragraphe">Nous cherchons à comprendre les mécanismes moléculaires et cellulaires qui sous-tendent la production et la maturation des cellules sanguines, en utilisant des outils de biologie moléculaire, de génétique et d'imagerie cellulaire.</p>
    <p class="paragraphe">Notre objectif ultime est de produire des cellules sanguines synthétiques de haute qualité qui pourraient un jour être utilisées pour traiter des maladies liées au sang, telles que l'anémie, le cancer et les troubles de la coagulation.</p>

